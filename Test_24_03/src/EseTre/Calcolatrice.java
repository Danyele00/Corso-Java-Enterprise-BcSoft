package EseTre;

public class Calcolatrice {
	
	public static double somma (double numeroUno, double numeroDue) {
		
		return numeroUno + numeroDue;
	}
	
	public static double sottrazione (double numeroUno, double numeroDue) {
		
		return numeroUno - numeroDue;
	}
	
	public static double moltiplicazione (double numeroUno, double numeroDue) {
		
		return numeroUno * numeroDue;
	}
	
	public static double divisione (double numeroUno, double numeroDue) {
		
		return numeroUno / numeroDue;
	}
}


